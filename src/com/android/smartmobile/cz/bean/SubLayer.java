package com.android.smartmobile.cz.bean;

import java.util.List;

public class SubLayer {
	String name = "";
	String titleField = "";
	String subTitleField = "";
	String canQuery = "";
	String canStatistics = "";
	String symbol = "";
	List<QueryField> queryFields;
	String url = "";
	String tableName = "";
	String geoType = "";

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getTitleField() {
		return titleField;
	}

	public void setTitleField(String titleField) {
		this.titleField = titleField;
	}

	public String getSubTitleField() {
		return subTitleField;
	}

	public void setSubTitleField(String subTitleField) {
		this.subTitleField = subTitleField;
	}

	public String getCanQuery() {
		return canQuery;
	}

	public void setCanQuery(String canQuery) {
		this.canQuery = canQuery;
	}

	public String getCanStatistics() {
		return canStatistics;
	}

	public void setCanStatistics(String canStatistics) {
		this.canStatistics = canStatistics;
	}

	public String getSymbol() {
		return symbol;
	}

	public void setSymbol(String symbol) {
		this.symbol = symbol;
	}

	public List<QueryField> getQueryFields() {
		return queryFields;
	}

	public void setQueryFields(List<QueryField> queryFields) {
		this.queryFields = queryFields;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	public String getGeoType() {
		return geoType;
	}

	public void setGeoType(String geoType) {
		this.geoType = geoType;
	}

}
